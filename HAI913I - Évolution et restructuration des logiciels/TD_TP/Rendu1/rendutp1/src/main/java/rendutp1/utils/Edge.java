package rendutp1.utils;

public class Edge {
	
	String nodeSource;
	String nodeTarget;
	
	public Edge (String nodeSource, String nodeTarget) {
		this.nodeSource = nodeSource;
		this.nodeTarget = nodeTarget;
	}

	public String getNodeSource() {
		return nodeSource;
	}

	public String getNodeTarget() {
		return nodeTarget;
	}
	
	

}
